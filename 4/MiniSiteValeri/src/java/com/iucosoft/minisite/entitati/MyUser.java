package com.iucosoft.minisite.entitati;

import java.util.Objects;

/**
 *
 * @author iucosoft7
 */
public class MyUser {

    int id;
    String username;
    String password;
    boolean isActivated;

    public MyUser() {
    }

    public MyUser(int id, String username, String password, boolean isActivated) {
        this.id = id;
        this.username = username;
        this.password = password;
        this.isActivated = isActivated;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public boolean isIsActivated() {
        return isActivated;
    }

    public void setIsActivated(boolean isActivated) {
        this.isActivated = isActivated;
    }

    @Override
    public int hashCode() {
        int hash = 3;
        hash = 11 * hash + this.id;
        hash = 11 * hash + Objects.hashCode(this.username);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final MyUser other = (MyUser) obj;
        if (this.id != other.id) {
            return false;
        }
        if (!Objects.equals(this.username, other.username)) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "MyUser{" + "id=" + id + ", username=" + username + ", isActivated=" + isActivated + '}';
    }

}
